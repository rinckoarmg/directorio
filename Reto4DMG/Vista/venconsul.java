    public void mostrar(int op){
        Alumnodao aBO = new Alumnodao();
        resul.setEditable(false);
        try {
            if (op==1){
            Alumno alumno = aBO.obtener(coi.getText());
            resul.setText(alumno.toString()); 
            } else if (op==2){
            if(!aBO.listado(ape.getText(), 1).isEmpty()) { 
            Iterator iterador = aBO.listado(ape.getText(), 1).listIterator(); //el objeto iterador que recorre el directorio
            while(iterador.hasNext()) {
                resul.append(iterador.next() + "\n"); //el m√©todo append agrega el contenido de persona al area de texto
                }
            }
            } else if (op==3){
            if(!aBO.listado(pro.getText(), 2).isEmpty()) { 
            Iterator iterador = aBO.listado(pro.getText(), 2).listIterator(); //el objeto iterador que recorre el directorio
            while(iterador.hasNext()) {
                resul.append(iterador.next() + "\n"); //el m√©todo append agrega el contenido de persona al area de texto
                }
            }
            } else if (op==4){
            if(!aBO.listado(fec.getText(), 3).isEmpty()) { 
            Iterator iterador = aBO.listado(fec.getText(), 3).listIterator(); //el objeto iterador que recorre el directorio
            while(iterador.hasNext()) {
                resul.append(iterador.next() + "\n"); //el m√©todo append agrega el contenido de persona al area de texto
                }
            }
            } else {
            if(!aBO.buscarCel(Long.parseLong(cel.getText())).isEmpty()) { 
            Iterator iterador = aBO.buscarCel(Long.parseLong(cel.getText())).listIterator(); //el objeto iterador que recorre el directorio
            while(iterador.hasNext()) {
                resul.append(iterador.next() + "\n"); //el m√©todo append agrega el contenido de persona al area de texto
                }
            }
        }
        } catch (Exception e) {
            resul.append("No hay resultados para esa consulta");
        }

    }
    
