    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        Alumnodao aBO = new Alumnodao();
        ventprincipal ini= new ventprincipal();
        String nombre = nom.getText();
        String apellido = ape.getText();
        String fech = fec.getText();
        String maili = coi.getText();
        String mailp = cop.getText();
        long celular = Long.parseLong(cel.getText());
        long telef = Long.parseLong(tel.getText());
        String prog = pro.getText();
        
        try {
            aBO.crear(nombre, apellido, fech, maili, mailp, celular, telef, prog);
            ini.setVisible(true);
            this.setVisible(false);
            this.setDefaultCloseOperation(this.EXIT_ON_CLOSE);
            this.dispose();
        } catch (IOException ex) {
            Logger.getLogger(ventcrear.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_agregarActionPerformed

